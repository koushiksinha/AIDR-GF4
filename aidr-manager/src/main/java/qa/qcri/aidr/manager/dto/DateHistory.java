package qa.qcri.aidr.manager.dto;

public class DateHistory {

    private String taskcreated;

    private String taskcompleted;

    private String taskpresented;

    private String taskpulled;

    public String getTaskcreated() {
        return taskcreated;
    }

    public void setTaskcreated(String taskcreated) {
        this.taskcreated = taskcreated;
    }

    public String getTaskcompleted() {
        return taskcompleted;
    }

    public void setTaskcompleted(String taskcompleted) {
        this.taskcompleted = taskcompleted;
    }

    public String getTaskpresented() {
        return taskpresented;
    }

    public void setTaskpresented(String taskpresented) {
        this.taskpresented = taskpresented;
    }

    public String getTaskpulled() {
        return taskpulled;
    }

    public void setTaskpulled(String taskpulled) {
        this.taskpulled = taskpulled;
    }
}
